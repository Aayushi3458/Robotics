           <h4 style="text-align:left"><b>Guidelines for the Evaluation of DD-ROBOCON 2022 Stage 2</b></h4>
           <p style="text-align:left">As a part of Stage II, all qualified teams from Stage I need to submit the following videos by the deadline specified on the website whilst adhering to the OFFLINE rulebook (link for submitting the videos will be shared soon):
            <br>
            For teams with both Robot (R1) and Robot (R2):
            <br>
            <ol style="text-align:left">
                <li><b>Shooting Mechanism to break Lagori (20 points):</b> In this video (maximum 30 seconds excluding Measurement), the Seeker Robot (R1) should throw 3 preloaded Seeker Balls to break the Lagori from its Start Zone. Specifically, videos should demonstrate the following functionalities:
                 <ul>
                  <li>Shooting Mechanism of R1 to break the Lagori. (10)</li>
                  <li>Effectiveness of the mechanism demonstrating breaking of Lagori. (10)</li>
                 </ul> 
                Note: Measurement of distances should be clearly shown in the video.
                </li>
                <li><b>Lagori Piling (30 points):</b> In this video (maximum 60 seconds with BOH placed), the Seeker Robot (R2) should start from its Start Zone, enter Lagori zone to pile up broken lagori discs. The following aspects of the robot will be considered during evaluation:
                 <ul>
                  <li>Movement of the Seeker R2. (5)</li>
                  <li>Picking Lagori disc(s) from the ground, broken in video 1. (10)</li>
                  <li>Piling up Lagori discs should be clearly seen in the same video. (15)</li>
                 </ul> 
              </li>
                <li><b>Hitting Mechanism to break Ball on Head (BOH) (25 points):</b> In this video, the Hitter Robot (R1) should start from its Start Zone, receive Hitter Balls thrown by R2 to break the Ball on Head (BOH). Specifically, the videos should demonstrate the following functionalities:
              <ul>
                <li>Hitting Mechanism of R1 to break the Ball on Head (BOH). (10)</li>
                <li>Effectiveness and range (by targeting BOH which is movable in horizontal plane attaching with BOH plate at height ranging between 1,200-1,250mm) of the hitting mechanism. (15)</li>
              </ul>
              Note: Measurement of distances should be clearly shown in the video.
              </li>
                <li><b>Ball Picking and Passing (25 points):</b> In this video, the Hitter Robots (R1 & R2 or R2 only) should start from its Start Zone, move to pick one or more ball(s) from the ball rack, and Hitter R2 passes balls to the Hitter R1. Passing either one by one or multiple balls together should be shown (pass at least one ball from each ball rack). The following aspects of the robot will be considered during evaluation:
              <ul>
                <li>Movement of the Hitter Robot (R1 & R2 or R2 only). If Hitter R1 also moves, then movement back to its start zone should be shown so that it can aim BOH perfectly. (5)</li>
                <li>Picking ball(s) from the ball rack. (10)</li>
                <li>Passing the ball(s) to, and receiving by, Hitter Robot (R1) zone should be seen in the same video. (10)</li>
              </ul>
              </li>
            </ol>
           </p>
           
           <h4 style="text-align:left"><b>Submission Details</b></h4>
           <p style="text-align:left">
           For each mechanism, <b>one separate video</b> must be prepared. Additional instructions are given below for all the videos that should be kept in mind while preparing the videos:
            <ol style="text-align:left">
                <li>All the files should be uploaded to a single Google Drive folder which is accessible by the judges. Only the link should be submitted via a Google Form (to be released soon).</li>
                <li>The combined size of all the files <b>should not exceed 1 GB.</b></li>
                <li>Robot functionality should be demonstrated in a single, continuous shot.</li>
                <li>Use multiple cameras for better visualization with one camera showing the complete view.</li>
                <li>Compliance with competition rules will be considered during evaluation.</li>
                </ol>
                <p style="text-align:left">All videos <b>should be</b> named as <b>“Team Name demonstrated mechanism”</b> to ensure proper evaluation and be in the <b>.mp4</b> or <b>.avi</b> format only.    
